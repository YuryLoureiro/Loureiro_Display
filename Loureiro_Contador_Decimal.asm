INICIO:
	MOVLW TRISB ;PORTB OUT
	MOVWF FSR
	CLRF 0X00
	MOVLW TRISC ;PORTC OUT
	MOVWF FSR
	CLRF 0X00
	MOVLW TRISD ;PORTD OUT
	MOVWF FSR
	CLRF 0X00
	CLRF 0X20 ; variavel x
	CLRF 0X21 ; y
	CLRF 0X22 ; z
	CLRF 0X23 ; w
	CLRF 0X24 ; k

	GOTO CASA1

MILI:
	MOVLW 0x7E ; y = 7E
	MOVWF 0x25
DELAY_1:
	MOVLW 0x02 ; k = 02
	MOVWF 0x26
DELAY:
	INCFSZ 0x26,f
	GOTO DELAY
	INCFSZ 0x25,f
	GOTO DELAY_1
RETARDO:
	MOVLW 0xF3 ; MUDAR PARA F3
	MOVWF 0x27
LOOP_1:
	INCFSZ 0x27,f
	GOTO LOOP_1
	NOP
	RETURN

CASA0:
	CALL MILI
	INCF 0X20, F
	BTFSS 0X20, 3
	GOTO CASA0
	BTFSS 0X20, 1
	GOTO CASA0
	INCF 0x21, F
	GOTO CASA1


CASA1:
	CLRF 0X20
	BTFSC 0X21, 3
	BTFSS 0X21, 1
	GOTO HERE
	INCF 0x22, F
	GOTO CASA2

HERE:	BCF PORTC, 7
	BCF PORTD, 5
	BCF PORTD, 4
	BCF PORTD, 5
	BSF PORTC, 6
	MOVF 0X21, W
	CALL DIG
	MOVWF PORTB
	BSF PORTC, 6
	GOTO CASA0

CASA2:
	CLRF 0X21
	BTFSC 0X22, 2
	BTFSS 0X22, 1
	GOTO HERE2
	INCF 0x23, F
	GOTO CASA3

HERE2:
	MOVF 0X22, W
	CALL DIG
	MOVWF PORTB
	BCF PORTC, 6
	BSF PORTC, 7
	GOTO CASA0

CASA3:
	CLRF 0X22
	BTFSC 0X23, 3
	BTFSS 0X23, 1
	GOTO HERE3
	INCF 0x24, F
	GOTO CASA4

HERE3:
	MOVF 0X23, W
	CALL DIG
	MOVWF PORTB
	BCF PORTC, 7
	BCF PORTC, 6
	BSF PORTD, 4
	GOTO CASA0

CASA4:
	CLRF 0X23
	BTFSC 0X24, 2
	BTFSS 0X24, 1
	GOTO HERE4
	INCF 0x24, F
	GOTO CASA4

HERE4:
	MOVF 0X24, W
	CALL DIG
	MOVWF PORTB
	BCF PORTC, 6
	BCF PORTD, 4
	BSF PORTD, 5
	GOTO CASA0

DIG:	ADDWF PCL,F
	RETLW 0x3F ;0
	RETLW 0x06 ;1
	RETLW 0x5B ;2
	RETLW 0x4F ;3

	RETLW 0x66 ;4'
	RETLW 0x6D ;5'
	RETLW 0x7D ;6'
	RETLW 0x07 ;7'

	RETLW 0x7F ;8'
	RETLW 0x67 ;9'